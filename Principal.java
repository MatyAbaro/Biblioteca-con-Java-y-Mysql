    public void InsertarDatos(){
         
         try {
              String SQL = "insert into libros (titulo,autor,genero,estado) values (?,?,?,?)";
              PreparedStatement pst = con.prepareStatement(SQL);
              pst.setString(1,txtTitulo.getText());
              pst.setString(2,txtAutor.getText());
              int seleccionado = cboxGenero.getSelectedIndex();
              pst.setString(3,cboxGenero.getItemAt(seleccionado));
              int seleccionado1 = cboxEstado.getSelectedIndex();
              pst.setString(4,cboxEstado.getItemAt(seleccionado1));
              
              pst.execute();  //ejecuta la consulta
              
              JOptionPane.showMessageDialog(null, "Se ha registrado exitosamente");
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Error en el registro"+ e.getMessage());
             
         }
     }
     public void ActualizarDatos(){
         
         try {
              String SQL = "update libros set titulo=?,autor=?,genero=?,estado=? where id_libro=?";
              int filaSeleccionado=tablaBaseDatos.getSelectedRow();
              String dao=(String)tablaBaseDatos.getValueAt(filaSeleccionado,0);
              
              PreparedStatement pst = con.prepareStatement(SQL);
              pst.setString(1,txtTitulo.getText());
              pst.setString(2,txtAutor.getText());
              int seleccionado = cboxGenero.getSelectedIndex();
              pst.setString(3,cboxGenero.getItemAt(seleccionado));
              int seleccionado1 = cboxEstado.getSelectedIndex();
              pst.setString(4,cboxEstado.getItemAt(seleccionado1));
              pst.setString(5,dao);
              
              pst.execute();  //ejecuta la consulta
              
              JOptionPane.showMessageDialog(null, "Se ha editado exitosamente el registro");
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Error en la edicion del registro"+ e.getMessage());
             
         }
     }
   
