    public void eliminarLibro(){
        
        int filaSeleccionada = tablaBaseDatos.getSelectedRow();
        try {
            String SQL="delete from libros where id_libro="+tablaBaseDatos.getValueAt(filaSeleccionada,0);
            
            Statement st = con.createStatement();
            
            int n= st.executeUpdate(SQL);
            
            if (n>=0){
                
                JOptionPane.showMessageDialog(null,"Libro eliminado Correctamente!");
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null,"Error al eliminar libro!"+e.getMessage());
        }
        
    }
    public void buscarDatos(String valor){
        
        String[] titulos ={"id","Titulo","Autor","Genero","Estado"};
        String[] registros = new String[6];
        
        DefaultTableModel modelo = new DefaultTableModel(null,titulos);
        
        String SQL = "select * from libros where titulo like '%"+valor+"%'";
        
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            
            while (rs.next()){
                registros[0]= rs.getString("id_libro");
                registros[1]= rs.getString("titulo");
                registros[2]= rs.getString("autor");
                registros[3]= rs.getString("genero");
                registros[4]= rs.getString("estado");
                
                modelo.addRow(registros);
                
                
            }
            tablaBaseDatos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al mostrar los datos"+ e.getMessage());
        }
    }
    
    
